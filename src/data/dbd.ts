/** รายการทั้งหมดแบบแบน — สำหรับ datalist / autocomplete */
export const DBD_ALL_ITEMS: string[] = DBD_SECTORS.flatMap(s =>
  s.items.map(item => `[${s.code}] ${item}`)
);

/** รายการ Section Labels เท่านั้น */
export const DBD_SECTION_LABELS: string[] = DBD_SECTORS.map(s => `${s.label}`);

/** หา Sector จาก code */
export function getDBDSector(code: string): DBDSector | undefined {
  return DBD_SECTORS.find(s => s.code === code);
}
